#this code is methods for strings
#upper methods, this methods is for change lower case and capitalize
msg = input("write me ")
msg = msg.upper()
print(msg)
#lower methods, this methods is for change change capitalize and lower case
msg = msg.lower()
print(msg)
#strip methods, this methods quite empty space the begin and final 
msg = msg.strip()
print(msg)
#replace methods, this methods change the first string with second string 
msg = msg.replace("k","p")
print(msg)